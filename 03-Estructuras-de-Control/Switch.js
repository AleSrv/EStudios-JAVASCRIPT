//Sentencias Switch

let nota =5;

switch (nota){
    case 5:
        console.log("Felicidades es Sobresaliente")
        break;
    case 4:
        console.log("Buen trabajo, pero podria ser mejor")
        break;
    case 3:
        console.log("Has obtenido un suficiente")
        break;
    case 2:
        console.log("No has aprobado por poco")
        break;
    case 1:
        console.log("No has estudiado nada")
        break;
    default:
        console.log("ERROR")
        break;
}

